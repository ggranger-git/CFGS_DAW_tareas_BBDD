CREATE TABLE  CLIENTE(
dni VARCHAR2(9) PRIMARY KEY,
nombre VARCHAR2(20),
apellidos VARCHAR2(40),
direccion VARCHAR2(30),
telefono VARCHAR2(9),
n_viajes NUMBER(3)
);

/* ----- CREACI”N TABLA VIAJE -----*/
CREATE TABLE   VIAJE(
codigo VARCHAR2(5) PRIMARY KEY,
importe NUMBER(10,2),
fecha DATE,
nombre VARCHAR2(50),
cif_aso VARCHAR2(9)   /* Luego crearemos la restricciůn de FOREIGN KEY con ASOCIACI”N. */
);

/* ----- CREACI”N TABLA CONTRATAR -----*/
CREATE TABLE   CONTRATAR(
dni_cli VARCHAR2(9),
codigo_v VARCHAR2(5),
fecha_c DATE,
importe_c NUMBER(10,2),
CONSTRAINT con_PK PRIMARY KEY (dni_cli, codigo_v),
CONSTRAINT con_dni_FK foreign key(dni_cli) references CLIENTE(dni) on delete CASCADE,
CONSTRAINT con_cod_FK foreign key(codigo_v) references VIAJE(codigo) on delete CASCADE
);

/* ----- CREACI”N TABLA ASOCIACI”N -----*/
CREATE TABLE   ASOCIACION(
cif VARCHAR2(9) PRIMARY KEY,
nombre VARCHAR2(60),
poblacion VARCHAR2(30),
direccion VARCHAR2(40),
telefono VARCHAR2(12),
labor VARCHAR2(40),
pais VARCHAR2(150),
dni_vRes VARCHAR2(9)    /* Luego crearemos la restricciůn de FOREIGN KEY con VOLUNTARIO. */
);

/* ----- ACTUALIZACI”N TABLA VIAJE -----*/
/* Ahora que ya estŠ crada la tabla ASOCIACION creamos la clave ajena en la tabla VIAJE que referencia a esta tabla */
ALTER TABLE VIAJE ADD CONSTRAINT via_cif_FK FOREIGN KEY(cif_aso) REFERENCES ASOCIACION(cif) ON DELETE CASCADE;


/* ----- CREACI”N TABLA SEDE -----*/
CREATE TABLE   SEDE(
nSede NUMBER(3),
cif_aso VARCHAR2(9),
ciudad VARCHAR2(30),
constraint sed_PK PRIMARY KEY (nSede, cif_aso),
constraint sed_cif_FK FOREIGN KEY(cif_aso) REFERENCES ASOCIACION(cif) ON DELETE CASCADE
);

/* ----- CREACI”N TABLA VOLUNTARIO -----*/
CREATE TABLE   VOLUNTARIO(
dni VARCHAR2(9) PRIMARY KEY,
nombre VARCHAR2(20),
apellidos VARCHAR2(40),
telefono VARCHAR2(9),
direccion VARCHAR2(40),
fecha_in DATE,
pais_na VARCHAR2(20),
tipo_ayuda VARCHAR2(20),
cif_asoTra VARCHAR2(9),
puntos INT,
constraint vol_cif_FK FOREIGN KEY(cif_asoTra) REFERENCES ASOCIACION(cif) ON DELETE CASCADE
);




/* ----- ACTUALIZACI”N TABLA VOLUNTARIO -----*/
/* Ahora que ya estŠ crada la tabla VOLUNTARIO creamos la clave ajena en la tabla ASOCIACION que referencia a esta tabla */
ALTER TABLE ASOCIACION ADD CONSTRAINT aso_dni_FK FOREIGN KEY(dni_vRes) REFERENCES VOLUNTARIO(dni) ON DELETE CASCADE;


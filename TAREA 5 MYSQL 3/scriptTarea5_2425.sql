/* ----- CREACIÓN TABLA CLIENTE -----*/
CREATE TABLE IF NOT exists CLIENTE(
dni VARCHAR(9) PRIMARY KEY,
nombre VARCHAR(20),
apellidos VARCHAR(40),
direccion VARCHAR(30),
telefono VARCHAR(9),
n_viajes INT
);

/* ----- CREACIÓN TABLA VIAJE -----*/
CREATE TABLE IF NOT exists VIAJE(
codigo VARCHAR(5) PRIMARY KEY,
importe MEDIUMINT UNSIGNED,
fecha DATE,
nombre VARCHAR(50),
cif_aso VARCHAR(9)   /* Luego crearemos la restricción de FOREIGN KEY con ASOCIACIÓN. */
);

/* ----- CREACIÓN TABLA CONTRATAR -----*/
CREATE TABLE IF NOT exists CONTRATAR(
dni_cli VARCHAR(9),
codigo_v VARCHAR(5),
fecha_c DATE,
importe_c MEDIUMINT UNSIGNED,
CONSTRAINT con_PK PRIMARY KEY (dni_cli, codigo_v),
CONSTRAINT con_dni_FK foreign key(dni_cli) references CLIENTE(dni) on delete CASCADE on update CASCADE,
CONSTRAINT con_cod_FK foreign key(codigo_v) references VIAJE(codigo) on delete CASCADE on update CASCADE
);

/* ----- CREACIÓN TABLA ASOCIACIÓN -----*/
CREATE TABLE IF NOT exists ASOCIACION(
cif VARCHAR(9) PRIMARY KEY,
nombre VARCHAR(60),
poblacion VARCHAR(30),
direccion VARCHAR(40),
telefono VARCHAR(12),
labor VARCHAR(40),
pais VARCHAR(150),
dni_vRes VARCHAR(9)    /* Luego crearemos la restricción de FOREIGN KEY con VOLUNTARIO. */
);

/* ----- ACTUALIZACIÓN TABLA VIAJE -----*/
/* Ahora que ya está crada la tabla ASOCIACION creamos la clave ajena en la tabla VIAJE que referencia a esta tabla */
ALTER TABLE VIAJE ADD CONSTRAINT via_cif_FK FOREIGN KEY(cif_aso) REFERENCES ASOCIACION(cif) ON DELETE CASCADE ON UPDATE CASCADE;


/* ----- CREACIÓN TABLA SEDE -----*/
CREATE TABLE IF NOT exists SEDE(
nSede TINYINT,
cif_aso VARCHAR(9),
ciudad VARCHAR(30),
constraint sed_PK PRIMARY KEY (nSede, cif_aso),
constraint sed_cif_FK FOREIGN KEY(cif_aso) REFERENCES ASOCIACION(cif) ON DELETE CASCADE ON UPDATE CASCADE
);

/* ----- CREACIÓN TABLA VOLUNTARIO -----*/
CREATE TABLE IF NOT exists VOLUNTARIO(
dni VARCHAR(9) PRIMARY KEY,
nombre VARCHAR(20),
apellidos VARCHAR(40),
telefono VARCHAR(9),
direccion VARCHAR(40),
fecha_in DATE,
pais_na VARCHAR(20),
tipo_ayuda VARCHAR(20),
cif_asoTra VARCHAR(9),
puntos INT,
constraint vol_cif_FK FOREIGN KEY(cif_asoTra) REFERENCES ASOCIACION(cif) ON DELETE CASCADE ON UPDATE CASCADE
);




/* ----- ACTUALIZACIÓN TABLA VOLUNTARIO -----*/
/* Ahora que ya está crada la tabla VOLUNTARIO creamos la clave ajena en la tabla ASOCIACION que referencia a esta tabla */
ALTER TABLE ASOCIACION ADD CONSTRAINT aso_dni_FK FOREIGN KEY(dni_vRes) REFERENCES VOLUNTARIO(dni) ON DELETE CASCADE ON UPDATE CASCADE;

